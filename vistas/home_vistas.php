   <h1 class="mt-5">Vision</h1>
    <p class="lead">Ser garante de la igualdad jurídica y real de las mujeres como instancias rectora 
        de las políticas públicas con perspectivas de género. Convertirnos en una institución que brinde 
        protección y que sea fiador de los derechos de las mujeres, contenidos estos en la Constitución de
         la República Bolivariana de Venezuela. A su vez incorporar la mujer al acceso real a la justicia,
          el fortalecimiento del proceso revolucionario socialista y chavista en el municipio Independencia.
           IMMUFA, estima que las relaciones, acciones y actuaciones tanto a lo interno como a lo externo se
            basan en la transparencia, lealtad, integridad, honorabilidad, pulcritud y rectitud. IMMUFA,
             considera la voluntad de servicio como la consagración del servidor o servidora pública con un sentido de excelencia, en la atención de la población.
    </p> 
       <h1>Mision</h1> 
       <p class="lead">Es una institución concebida para proteger, orientar y mejorar la condición de vida incorporando a la mujer al desarrollo social, político y económico.
         Así como, ejercer las funciones de formulación, ejecución, dirección coordinación, supervisión y evaluación de las políticas públicas con perspectivas de
          género, garantizando la igualdad de oportunidades de las mujeres y promoviendo su participación patagónico en diferentes ámbitos a nivel territorial.
           Además garantizar a todas las mujeres del municipio independencia, el pleno ejercicio de sus derechos cuando los mismos han sido vulnerados asegurar el
            acceso rápido, transparente y eficaz antes los   organismos con competencia. De igual forma apoyar, organizar, educar y fortalecer a las comunidades 
